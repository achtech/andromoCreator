package com.how.achtech;

public class DataUser {
	private String linkWikiHow;
	private String linkLogo;
	private String linkBackground;
	private String folderDestination;
	/**
	 * @return the linkWikiHow
	 */
	public String getLinkWikiHow() {
		return linkWikiHow;
	}
	/**
	 * @param linkWikiHow the linkWikiHow to set
	 */
	public void setLinkWikiHow(String linkWikiHow) {
		this.linkWikiHow = linkWikiHow;
	}
	/**
	 * @return the linkLogo
	 */
	public String getLinkLogo() {
		return linkLogo;
	}
	/**
	 * @param linkLogo the linkLogo to set
	 */
	public void setLinkLogo(String linkLogo) {
		this.linkLogo = linkLogo;
	}
	/**
	 * @return the linkBackground
	 */
	public String getLinkBackground() {
		return linkBackground;
	}
	/**
	 * @param linkBackground the linkBackground to set
	 */
	public void setLinkBackground(String linkBackground) {
		this.linkBackground = linkBackground;
	}
	/**
	 * @return the folderDestination
	 */
	public String getFolderDestination() {
		return folderDestination;
	}
	/**
	 * @param folderDestination the folderDestination to set
	 */
	public void setFolderDestination(String folderDestination) {
		this.folderDestination = folderDestination;
	}

	
}
